package edu.talles.repeticao;

public class ExemploFor {
    public static void main(String[] args) {
        for (int carneiros = 1; carneiros<=20; carneiros += 2){
            System.out.println("Carneiros "+carneiros);
        }
        int arroz=1;
        for (;arroz<=20;){
            System.out.println(arroz);
            arroz++;
        }
    }
}
